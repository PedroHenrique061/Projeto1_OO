package ClassesProjeto;

public abstract class AlbumFigurinhas {

	protected int id;
	protected int quantidadeDePaginas;
	protected int quantidadeDeFigurinhas;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getQuantidadeDePaginas() {
		return quantidadeDePaginas;
	}
	public void setQuantidadeDePaginas(int quantidadeDePaginas) {
		this.quantidadeDePaginas = quantidadeDePaginas;
	}
	public int getQuantidadeDeFigurinhas() {
		return quantidadeDeFigurinhas;
	}
	public void setQuantidadeDeFigurinhas(int quantidadeDeFigurinhas) {
		this.quantidadeDeFigurinhas = quantidadeDeFigurinhas;
	}
	
	
}

